#include "../so_long.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s != (char)c && *s)
		s++;
	if (*s == (char)c)
		return ((char *) s);
	return (NULL);
}

void	ft_free_game(t_game *game)
{
	int	i;

	i = 0;
	while (game->map.map[i])
	{
		free(game->map.map[i]);
		i++;
	}
	free(game->map.map);
	exit(1);
}

size_t	ft_strlen(char *s)
{
	size_t	l;

	l = 0;
	while (s[l])
		l++;
	return (l);
}

void	ft_error(t_game *game, char *message)
{
	printf("%sso_long: %s%s", RED, message, RESET);
	if (game)
		ft_free_game(game);
	exit(1);
}
